// // Division
output = p / z;
console.log("p / z =", output);

// // Multiplication
output = p * z;
console.log("p * z=", output);

// Power
output = p ** w;
console.log(output);

// Modulo
// Give the remainder after division
output = w % p;
console.log("w % p =", output);

